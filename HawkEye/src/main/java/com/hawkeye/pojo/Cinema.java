package com.hawkeye.pojo;


public class Cinema {

  private Integer id;
  private String cinemaName;
  private String detailedAddress;
  private String cinemaLandline;
  private Integer cinemaBrandId;
  private Integer cityId;
  private Integer areaId;
  private Integer streetId;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public String getCinemaName() {
    return cinemaName;
  }

  public void setCinemaName(String cinemaName) {
    this.cinemaName = cinemaName;
  }


  public String getDetailedAddress() {
    return detailedAddress;
  }

  public void setDetailedAddress(String detailedAddress) {
    this.detailedAddress = detailedAddress;
  }


  public String getCinemaLandline() {
    return cinemaLandline;
  }

  public void setCinemaLandline(String cinemaLandline) {
    this.cinemaLandline = cinemaLandline;
  }


  public Integer getCinemaBrandId() {
    return cinemaBrandId;
  }

  public void setCinemaBrandId(Integer cinemaBrandId) {
    this.cinemaBrandId = cinemaBrandId;
  }


  public Integer getCityId() {
    return cityId;
  }

  public void setCityId(Integer cityId) {
    this.cityId = cityId;
  }


  public Integer getAreaId() {
    return areaId;
  }

  public void setAreaId(Integer areaId) {
    this.areaId = areaId;
  }


  public Integer getStreetId() {
    return streetId;
  }

  public void setStreetId(Integer streetId) {
    this.streetId = streetId;
  }

}
